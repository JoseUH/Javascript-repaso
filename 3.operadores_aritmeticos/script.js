// operadores aritmeticos

// suma -> +
// resta -> -
// multiplicacion -> * 
// division -> /
// resto -> %

const numA = 5
const numB = 10

console.log("Esto es una suma", numA + numB);
console.log("Esto es una resta", numA - numB);
console.log("Esto es una multiplicacion", numA * numB);
console.log("Esto es una division", numA / numB);
console.log("Esto es el resto", numA % numB);



